/*
Como você pode verificar se um número está dentro de um intervalo de 
10 a 20 em JavaScript usando operadores lógicos?

*/

let num = 15;
if (num >= 10 && num <= 20) {
    console.log("O número está dentro de um intervalo de 10 a 20");
}else{
    console.log("O número não está dentro de um intervalo de 10 a 20");
}